# Upper Case
a = "Hello, World!"
print(a.upper())


# Lower Case
b = "Hello, World!"
print(b.lower())


# Remove Whitespace
e = " Hello, World! "
print(e.strip()) # returns "Hello, World!"


# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))


# Split String
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']