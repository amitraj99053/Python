# Comparison Operators
x = 10
y = 20

print(x == y) 
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x is y)
print()

print(x is not y)
print(x < 15 and y > 15)
print(x < 15 or y < 15)
print(not(x < 15))
print((x + 5) == y)
print((x * 2) != y)
print((y / 2) >= x)
print((y - 5) <= x)
print((x + y) > 25)
print((x - y) < 0)
print((x ** 2) == 100)
print((y // 3) != 6)
print((x % 3) < 2)
print((y % 7) > 5)