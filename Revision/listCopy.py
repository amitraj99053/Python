# Make a copy of a list with the copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)
print()


# Make a copy of a list with the list() method
mylist2 = list(thislist)
print(mylist2)
print()


# Make a copy of a list with the : operator
mylist3 = thislist[:]
print(mylist3)
